let count = 5;
let result = count++; // post-increment
console.log(result); // 5
console.log(count);  // 6

let count2 = 5;
let result2 = ++count2; // pre-increment
console.log(result2); // 6

let score = 10;
let result3 = score--; // post-decrement
console.log(result3); // 10
console.log(score);   // 9

let score2 = 10;
let result4 = --score2; // pre-decrement
console.log(result4); // 9
